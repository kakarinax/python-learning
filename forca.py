import random

def jogo_forca():

    print_welcome()
    palavra_secreta = choose_word()

    letras_corretas = init_letras_corretas(palavra_secreta)

    enforcou = False
    acertou = False
    erros = 0

    print(f"Palavra contém {letras_corretas} letras ")

    while not enforcou and not acertou:

        chute = pede_chute()

        if chute in palavra_secreta:
            chute_correto(chute, letras_corretas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_corretas
        print(letras_corretas)

    if acertou:
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

def print_welcome():
    print("************************************")
    print("Bem vindo!")
    print("************************************")

def choose_word():
    arquivo = open("palavras.txt", "r")
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = random.randrange(0, len(palavras))

    palavra_secreta = palavras[numero].upper()
    return palavra_secreta

def init_letras_corretas(palavra):
    return ["_" for letra in palavra]

def pede_chute():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()
    return chute

def chute_correto(chute, letras_corretas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_corretas[index] = letra
        index += 1

